public class L01_SQL_Basics {
    public static void main(String[] args) {
        // SQL: Structured Query Language
        // SQL is a standard language for storing, manipulating, and retrieving data in databases.

        // Features of SQL:
        // 1. SQL has well defined standards.
        // 2. SQL is easy to learn and use.
        // 3. With SQL we can create multiple views
        // 4. SQL is portable in nature.

        // Types of databases: Distributed, Object-oriented, Centralized, Operational, Graph, NoSQL, Cloud, Relational

        // Types of SQL commands:
        // 1. DDL (Data Definition Language): CREATE, ALTER, DROP, TRUNCATE
        // 2. DML (Data Manipulation Language): INSERT, UPDATE, DELETE  
        // 3. DCL (Data Control Language): GRANT, REVOKE
        // 4. TCL (Transaction Control Language): COMMIT, ROLLBACK, SAVEPOINT

        // SQL Constraints:
        // 1. NOT NULL
        // 2. UNIQUE
        // 3. PRIMARY KEY
        // 4. FOREIGN KEY
        // 5. CHECK
        // 6. DEFAULT
        // 7. INDEX

        // SQL Operators:
        // 1. Arithmetic Operators: +, -, *, /, %
        // 2. Comparison Operators: =, !=, <, >, <=, >=
        // 3. Logical Operators: AND, OR, NOT
        // 4. Bitwise Operators: &, |, ^, ~, <<, >>

        // SQL Views:
        // A view is a virtual table based on the result-set of an SQL statement.
        // A view contains rows and columns, just like a real table.
        // The fields in a view are fields from one or more real tables in the database.
    }
}
